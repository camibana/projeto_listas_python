# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

tamquadr = float(input('Digite a medida de um dos lados do quadrado:'))
areaquadr = tamquadr * 2
aqdobro = areaquadr * 2
print('A área do quadrado é de {:.2f}, e o seu dobro é {:.2f}.'.format(areaquadr, aqdobro))